package com.company;

public class Clothes_Size {
    String size;
    int num_size;

    public Clothes_Size(String size, int num_size){
        this.size = size;
        this.num_size = num_size;
    }

    public String getSize(){
        return this.size;
    }

    public int getNumSize(){
        return this.num_size;
    }
}
